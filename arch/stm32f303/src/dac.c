void init_dac(void)
{
    //! enable DAC1 clock
    RCC_APB1ENR |= BIT29;
    //!  set DAC1 channel 1 to trigger by TIM6;
    DAC_CR &= ~(BIT3 | BIT4 | BIT5);
    //!  enable DAC1 channel 1 buffering
    DAC_CR |= BIT1;
    //!  enable DAC1 channel 1 trigger;
    DAC_CR |= BIT2;
    //!  disable DAC1 channel 1 wave generation 
    DAC_CR &= ~(BIT6 | BIT7);
    //!  DAC1 channel 1 DMA enable
    DAC_CR |= BIT12;
    //!  enable DAC1 channel 1;
    DAC_CR |= BIT0;
}

void write_dac_8RA(uint8_t value)
{
    //! write to DAC1 channel 1 
    //! 8 bit right alligned data register
    DAC_DHR8R1 = value;
}

uint32_t get_sine_wave(void) {
    return (uint32_t) &sine_wave;
}
